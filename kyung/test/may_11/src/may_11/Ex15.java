package may_11;

public class Ex15 {

		public static void main(String[] args) {
			int a, b;
			int result;
			
			a = 50;
			b = 100;
			
			result = a + b;
			System.out.println(a + "+" + b +"=" + result);
			
			result = a - b;
			System.out.println(a + "-" + b +"=" + result);
			
			result = a * b;
			System.out.println(a + "*" + b +"=" + result);
			
			result = a / b;
			System.out.println(a + "/" + b +"=" + result);
		}
}
