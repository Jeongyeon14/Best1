package may_11;

public class Ex21_1 {

	public static void main(String[] args) {
		// 10진수 %d  8진수%d  16진수%x
		
		int A = 'a';
		int B = 'b';
		int C = 'c';
		
		System.out.printf("%d \n",A);
		
		System.out.printf("a의 10진수==> %d \n",A);
		System.out.printf("b의 10진수==> %d \n",B);
		System.out.printf("c의 10진수==> %d \n\n",C );
		
		System.out.printf("a의 8진수==> %#o \n",A);
		System.out.printf("b의 8진수==> %#o \n",B);
		System.out.printf("c의 8진수==> %#o \n\n",C);
		
	
		System.out.printf("a의 16진수==> %#x \n",A);
		System.out.printf("b의 16진수==> %#x \n",B);
		System.out.printf("b의 16진수==> %#x \n\n",C);
	
		
		
		
		
		
		
		
		
	}
}
